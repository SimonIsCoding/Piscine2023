int	ft_check_argc_is_2(char **argv)
{
	int	i;

	i = 0;
	while (argv[1][i] != '\0')
	{
		if (!(argv[1][i] >= '0' && argv[1][i] <= '9'))
		{
			write(1, "Error\n", 6);
			return (0);
		}
	}
	calc(argv[1], "numbers.dict");
	return (1);
}
