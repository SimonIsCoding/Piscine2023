void	put_char2(int x, int y)
{
	if (x == 0 || y == 0)
	{// 1write(1, "", 1);
	}
	else if (x > 0 && y > 0)
	{
		if ((x == y && x == 1) || (x == y && y == y - 1))
			write(1, "A", 1);
	}
}

