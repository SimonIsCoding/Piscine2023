#include <unistd.h>

void	put_char(int x, int y, int f, int c)
{
	if (x >= 1 && y >= 1)
	{
		if ((f == 0 && c == 0) || (f == (y - 1) && c == (x - 1)))
			write(1, "A", 1);
		if ((f == (y - 1) && c == 0) || (f == 0 && c == (x - 1)))
			write(1, "C", 1);
		if ((f == 0 || f == (y - 1)) && c > 0 && c < (x - 1))
			write(1, "B", 1);
		if ((f > 0 && f < y - 1) && (c == 0 || c == x - 1))
			write(1, "B", 1);
		if ((f > 0 && f < y - 1) && (c > 0 && c < x - 1))
			write(1, " ", 1);
	}
}
